// 2: tarike se bhi routes set kar sakte hai:-
const router = createBrowserRouter(
  createRoutesFromElements(
    <Route path='/' element={<Layout />}>
      <Route path='' element={<Home />} />
      <Route path='about' element={<About />} />
      <Route path='contact' element={<Contact />} />
      <Route path='user/:userid' element={<User />} /> {/* check:- http://localhost:5173/user/123 */}
      <Route 
      loader={githubInfoLoader} // yahi par API call likh sakte CallBack use karke(new feature hai react-Router dom ka)
      path='github' 
      element={<Github />}
       />
    </Route>
  )
)

ReactDOM.createRoot(document.getElementById('root')).render(
  <React.StrictMode>
    {/* router banna hai */}
    {/* RouterProvider ek warraper hai uske andar sab lapate diya gaya hai */}
    <RouterProvider router={router} />
  </React.StrictMode>,
)
